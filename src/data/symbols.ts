// ── Core matching function ────────────────────────────────────────────────────
export function articleMentionsSymbol(
  article: { headline: string; article_body: string; asset_tags: string[] },
  sym: MarketSymbol
): boolean {
  // 1. asset_tags exact match (case-insensitive)
  if (article.asset_tags?.some(t => sym.aliases.includes(t.toLowerCase()))) return true;

  // 2. Text search in headline + body
  const text = `${article.headline} ${article.article_body ?? ''}`.toLowerCase();
  return sym.aliases.some(alias => {
    const idx = text.indexOf(alias);
    if (idx === -1) return false;
    const before = idx === 0 ? ' ' : text[idx - 1];
    const after  = idx + alias.length >= text.length ? ' ' : text[idx + alias.length];
    return !/[a-z0-9]/.test(before) && !/[a-z0-9]/.test(after);
  });
}
